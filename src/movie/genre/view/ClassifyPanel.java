    private void classButtonClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_classButtonClicked
        if (validateInput()) {
            String title = titleText.getText().trim();
            String actors = actorsText.getText().trim();
            String directors = directorsText.getText().trim();
            String synopsis = synopsisText.getText().trim();
            Movie movie = new Movie(title, actors, directors, synopsis);
            String pmmResult = pmmClassifier.classify(movie);
            String knnResult = knnClassifier.classify(movie);
            pmmClassLabel.setText(pmmResult);
            knnClassLabel.setText(knnResult);
        }
    }//GEN-LAST:event_classButtonClicked

    private void selectComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectComboActionPerformed
        int pos = selectCombo.getSelectedIndex() + 1;
        try {
            JSONObject object = testArray.getJSONObject(pos);
            titleText.setText(object.getString("title"));
            actorsText.setText(object.getString("actors"));
            directorsText.setText(object.getString("director"));
            synopsisText.setText(object.getString("synopsis"));
            imdbClassLabel.setText(object.getString("genres"));

        } catch (JSONException ex) {
            Logger.getLogger(ClassifyPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_selectComboActionPerformed


