    void loadTestData() throws IOException, JSONException {
        StringBuilder builder = new StringBuilder();
        String line;
        try (BufferedReader read = new BufferedReader(new FileReader("resources/" + PMMClassifier.TEST_SRC))) {
            while ((line = read.readLine()) != null) {
                builder.append(line);
            }
        }
        testArray = new JSONArray(builder.toString());
        String[] titles = new String[testArray.length() + 1];
        titles[0] = "Select a movie.";
        for (int i = 0; i < testArray.length(); i++) {
            titles[i + 1] = testArray.getJSONObject(i).getString("title");
        }

        selectCombo.setModel(new javax.swing.DefaultComboBoxModel(titles));

    }

